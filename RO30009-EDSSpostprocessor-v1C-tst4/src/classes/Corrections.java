package classes;

public class Corrections {
	String Site_Number;
	String Wrong_Inv_name;
	String Corrected_Inv_name;

	public String getSite_Number() {
		return Site_Number;
	}

	public void setSite_Number(String site_Number) {
		Site_Number = site_Number;
	}

	public void setWrong_Inv_name(String wrong_Inv_name) {
		Wrong_Inv_name = wrong_Inv_name;
	}

	public String getCorrected_Inv_name() {
		return Corrected_Inv_name;
	}

	public void setCorrected_Inv_name(String corrected_Inv_name) {
		Corrected_Inv_name = corrected_Inv_name;
	}

	public String getWrong_Inv_name() {
		return Wrong_Inv_name;
	}

}
